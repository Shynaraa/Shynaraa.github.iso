function m1(){
	var x = document.getElementById('counter1');
	var y = document.getElementById('sum').value;
	if(x.value>0){
		x.value--;
		document.getElementById('sum').value = Number(y) - Number(700);
	}
}

function a1(){
	var x = document.getElementById('counter1');
	var y = document.getElementById('sum').value;
	x.value++;
	document.getElementById('sum').value = Number(700) + Number(y);
}

